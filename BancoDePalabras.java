public class BancoDePalabras
{
    // Crear una variable tipo string que contenga una palabra
    String palabra;
        
    /**
     * Constructor for objects of class BancoDePalabras
     */
    public BancoDePalabras(String palabra)
    {
        this.palabra = palabra;
    }

    public String dameUnaPalabra()
    {
        return palabra;
    }
    
}
